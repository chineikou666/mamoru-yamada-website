export function getBuildingById(id: number): Building | undefined {
  return buildings.find((b) => b.id === id);
}

export function getBuildingsByRegion(regionId: RegionId): Building[] {
  if (regionId === "all") return buildings;
  return buildings.filter((b) => b.region === regionId);
}
